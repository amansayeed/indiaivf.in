					<h3>Causes of Male Infertility</h3>
					<p> 
Male infertility is usually more difficult to diagnose than female infertility because symptoms do not show up early. The following are the most common causes of male infertility.
</p>
					<h5>1.Varicoceles</h5>
					<p> 
It is the most common cause of male infertility prevailing today but it is reversible. It is a swelling of the veins in the scrotum which can impact sperm quality. It can affect one or both testes. The majority of men with varicocele present no symptoms. It is believed the varicoceles raise the internal temperature of the scrotum which can lead to infertility, as sperm requires an optimal temperature to develop normally. Any fluctuation can cause abnormalities. Varicoceles have been associated with low sperm count, sperm morphology and sperm quality.
</p>
					<h5>2. Ejaculation Disorders</h5>
					<p>
Even if a man has healthy sperm, problems with ejaculation can prevent conception. They include premature ejaculation, anejaculation (the failure to ejaculate), and retrograde ejaculation in which semen enters the bladder during orgasm instead of coming out of the tip of the penis.
</p>
					<h5>3. Tubal issues</h5>
					<p>
Sperm travels through many different tubes before they are finally ejaculated. Blockage of any of these tubes may lead to low sperm count. One of the main reasons for sperm blockage could be deformities or a complete absence of the vas deferens. It can be a congenital disability or due to physical trauma. Other tubules which can also cause blockages include the epididymis, the urethra, and the ejaculatory ducts. Previous surgeries, infections, and cysts are some of the other common reasons for abnormalities within the tubules.
</p>
					<h5>4.Infections</h5>
					<p>4.Infections
Infections that are left untreated for a long time, can cause significant damage to the sperm and tubes. STIs such as gonorrhea, HIV and inflammation of the testicles, can affect sperm production or sperm health or can cause scarring that blocks the passage of sperm. 
</p>
					<h5>5.Lifestyle factors</h5>
					<p> 
Unhealthy lifestyle choices are one of the main causes of male infertility. Smoking and drinking are the most widespread causes of male infertility. Obesity and recreational drugs can also affect sperm count and quality. Additionally, bodybuilding supplements have also been linked to low fertility levels among men. Exposure to environmental factors such as heavy metals, harmful chemicals, and radiation can also affect fertility.
</p>
					<h5>6. Chromosomal defects:</h5>
					<p> Hereditary disorders, such as Klinefelter’s syndrome, when a male is born with two X chromosomes and one Y chromosome instead of one X and one Y  impacts the normal development of the male reproductive organs.
</p>
					
					
					<h3>Causes of Infertility in Women </h3>
					<p>
Infertility in women can sometimes be detected early because it can result from hormonal disruptions. Apart from hormonal factors, there are also multiple other factors which can cause infertility in women. The most common causes of infertility in women are:
</p>
					<h5>1. Endometriosis</h5><p>
It is the condition in which the tissue of the uterine lining grows outside of the uterus. Heavy and painful periods are the first symptoms of endometriosis. The uterine lining formed in anticipation of pregnancy sheds and is removed from the body during normal menstruation. During endometriosis, the uterine lining does not completely shed. Instead, it attaches to other parts of the body. Endometriosis accounts for around 30-50 percent of all cases of female infertility.
</p>                <h5>2. Irregular periods</h5>
					<p> 
When a woman regularly experiences irregular periods, then ovulation cycles can be unpredictable. PCOS (Polycystic Ovarian Syndrome) is one of the most common reasons for irregular periods in women and is also a major cause of female infertility. Apart from irregular periods, PCOS is also marked by excessive weight gain, painful periods, abnormal hair growth and skin disorders. Stress, emotional issues, sudden weight loss or weight gain can also lead to irregular periods.
</p>
					<h5>3. Age </h5>
					<p>
Quality of eggs is greatly affected by age of the women. Every passing year after 35 can lead to further deterioration in egg quality. Even if a woman has not reached menopause stage, she might still have issues in getting pregnant. The increase in the risk of a miscarriage also increases with age.
</p>
					<h5>4. Fallopian tube issues </h5>
					<p> 
After fertilization, egg needs to travel through the fallopian tube to reach the uterus. The issues with the fallopian tube can severely impact a woman’s chances of conceiving. 
</p>
					<h5>5.Lifestyle factors</h5>
					<p>
Smoking has as big an impact on female infertility same as in the case of males. Miscarriages are found to be more common among women who have a history of smoking. Recreational drugs like marijuana can also prevent conception. Overweight or underweight women might face issues with fertility. Women who are long-time users of birth control pills might also have trouble conceiving because these drugs interfere with the body’s normal hormone production.
</p>
					
