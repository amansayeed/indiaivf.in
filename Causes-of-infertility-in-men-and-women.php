	<p>Infertility is becoming a growing cause for concern in India. An infertility diagnosis could be shattering for the couples trying to start a family. Most doctors recommend couples to visit a fertility specialist if they have not been able to conceive even after a year of unprotected intercourse. This duration falls to six months for women above 35. According to current estimates, around 1 in 5 Indian couples are experiencing problems of infertility. By understanding the reasons and treatments for infertility, couples can take steps to manage their condition.  There are different causes for male and female infertility.
