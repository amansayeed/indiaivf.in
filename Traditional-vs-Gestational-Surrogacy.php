								<p>The term surrogacy is becoming increasingly popular day by day. For couples facing reproductive challenges, surrogacy is a lifeline of hope to have a child. A surrogate mother is a woman who carries a child for parents who are unable to have a child on their own. While considering surrogacy, one of the first decisions hopeful parents and potential surrogates have to make is the type of surrogacy they want to pursue.
There are two major types:
<ul style="margin-left:15px;">
<li>Gestational Surrogacy </li>
<li>Traditional Surrogacy</li>
</ul>

