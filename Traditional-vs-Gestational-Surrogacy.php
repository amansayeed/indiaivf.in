<p>In gestational surrogacy, a surrogate mother carries the child for the intended parents and has no genetic relationship with the baby. The embryos are created in the laboratory using <b>In-vitro fertilization (IVF) </b>which are then transferred into the surrogate’s uterus in the fertility clinic. The embryo is created using the intended mother’s or donor’s egg and the intended father’s or donor’s sperm. The intended parents will be named on the birth certificate although the gestational surrogate is identified as the birth mother, part of the legal process with this type of surrogacy establishes this fact. The intended parents go on to raise their child born through surrogacy once the baby is born.
</p>
<p>In traditional surrogacy, the surrogate mother acts as the egg donor and is the biological mother of the baby she is carrying. The embryos are created using a process termed<b> Intrauterine Insemination (IUI)</b> in which the sperm is taken from the intended father or a donor. This type of surrogacy is also called as full surrogacy or genetic surrogacy. As this process can be more legally and emotionally complicated, this form of surrogacy is now far less common than gestational surrogacy.
</P>
<p>It is significant for hopeful intended parents and potential surrogates to consider all of the following factors before deciding between traditional vs. gestational surrogacy:
</P>
<p><u><b>Medical Process:</b></u>  Usually, IUI is a simpler process that requires the surrogate to undergo fewer fertility treatments. Also, the intended mothers will not be needed to take fertility medications or undergo the egg retrieval process, as their eggs will not be used to create the embryo.</P>
<p><u><b>Legal Process:</b></u>  Traditional surrogacy laws are likely to be more complicated than gestational surrogacy laws. As the surrogate is the biological mother of the baby in traditional surrogacy, her parental rights need to be legally terminated after the baby is born. It means that the non-biological parent of the child will need to complete a step-parent adoption to obtain parental rights. These added legal procedures are not required in the gestational surrogacy process, as parentage is established before birth using a pre-birth order</P>
<p><u><b>Wait Time:</b></u>   Gestational surrogacy is preferred due to its less legal complications which get completed in a short period of time. 
</P>
<p><u><b>Costs: </b></u> The gestational surrogacy tends to be costlier than for traditional surrogacy as IUI is less expensive than IVF and tends to involve fewer medical procedures and fertility treatments.
</P>
<p>There are a number of pros and cons to be considered while debating traditional surrogacy vs. gestational surrogacy. Ultimately, it depends on each intended parent and prospective surrogate to consider their own goals and priorities when choosing the type of surrogacy that is right for them.
